package lab.Assignment;

public class MathDemo {
    public static void main(String[] args) {
        double x = 4.5;
        double y = 2.0;
        // Exponential and logarithmic functions
        System.out.println("e^x: " + Math.exp(x));
        System.out.println("Logarithm base 10 of x: " + Math.log10(x));
        System.out.println("Natural logarithm of x: " + Math.log(x));

        // Power and square root
        System.out.println("Square root of x: " + Math.sqrt(x));
        System.out.println("x raised to the power y: " + Math.pow(x, y));
    }
}
